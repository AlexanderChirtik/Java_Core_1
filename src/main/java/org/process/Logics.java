package org.process;

public class Logics {
    /**
     * Метод перемножения двух переменных
     * @param firstValue задается в классе Main
     * @param secondValue задается в классе Main
     * @return
     */
    public static int multiplication(int firstValue, int secondValue){
        return firstValue * secondValue;
    }

    /**
     * Метод деления первой переменной на вторую двух переменных
     * @param firstValue задается в классе Main
     * @param secondValue задается в классе Main
     * @return
     */
    public static int division(int firstValue, int secondValue){
        return firstValue / secondValue;
    }

    /**
     * Метод сложения двух переменных
     * @param firstValue задается в классе Main
     * @param secondValue задается в классе Main
     * @return
     */
    public static int addition(int firstValue, int secondValue){
        return firstValue + secondValue;
    }

    /**
     * Метод вычитания второй переменной из первой
     * @param firstValue задается в классе Main
     * @param secondValue задается в классе Main
     * @return
     */
    public static int subtraction(int firstValue, int secondValue){
        return firstValue - secondValue;
    }
}
